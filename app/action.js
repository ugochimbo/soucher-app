export const Buy_Soucher_Action = 'BuySoucher';

export const buySoucher = (soucherDetails) => {
    return {
        'type': Buy_Soucher_Action,
        'payload': soucherDetails
    }
};

export const swapSoucher = (soucherDetails) => {
    return {
        'type': Buy_Soucher_Action,
        'payload': soucherDetails
    }
};
